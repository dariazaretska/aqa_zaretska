package main.java.org.example;

public class Operations {
    public static void main(String[] args) {
        int a = 15;
        int b = 2;
        int sum = a + b;
        System.out.println(sum);

        int diff = b - a;
        System.out.println(diff);

        int mult = a * b;
        System.out.println(mult);

        float div = (float) a / b;
        System.out.println(div);
    }
}
